# list literal
fruits = ["apple", "mango", "orange"] 
print(fruits)

# tuple literal
numbers = (1, 2, 4) 
numbers = (1,2,3)
print(numbers)

# dictionary literal
alphabets = {'a':'apple', 'b':'ball', 'c':'cat'} 
print(alphabets)

# set literal :cannot have duplicate values
vowels = {'a', 'e', 'i' , 'o', 'u'} 
print(vowels)